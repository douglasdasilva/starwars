package sabres;

public class SabreDeLuz {
	private String tipoCristal = null;
	private String punho = null;
	
	public String getTipoCristal() {
		return tipoCristal;
	}
	public void setTipoCristal(String tipoCristal) {
		this.tipoCristal = tipoCristal;
	}
	public String getPunho() {
		return punho;
	}
	public void setPunho(String punho) {
		this.punho = punho;
	}
	
}
