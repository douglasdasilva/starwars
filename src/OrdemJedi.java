
public class OrdemJedi {
	private float percentualPaz = 0;
	private String nivelTreinamento = null;
	private String poder = null;
	
	public float getPercentualPaz() {
		return percentualPaz;
	}
	public void setPercentualPaz(float percentualPaz) {
		this.percentualPaz = percentualPaz;
	}
	public String getNivelTreinamento() {
		return nivelTreinamento;
	}
	public void setNivelTreinamento(String nivelTreinamento) {
		this.nivelTreinamento = nivelTreinamento;
	}
	public String getPoder() {
		return poder;
	}
	public void setPoder(String poder) {
		this.poder = poder;
	}
	
}
